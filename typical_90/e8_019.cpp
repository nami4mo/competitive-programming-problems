vector<vector<ll>> dp(401,vector<ll>(401,-1));
vector<ll> al(401,0);

ll rec(ll l, ll r){
    if(r-l<=0) return 0;
    if((r-l)%2==1) return INF;
    if(r-l==2) return dp[l][r]=abs(al[r-1]-al[l]);
    if(dp[l][r]!=-1) return dp[l][r];
    ll res=INF;
    for(int dist=1; dist<r-l; dist+=2){
        for(int left=l; left+dist<r; left+=2){
            ll right=left+dist;
            ll val=rec(l,left)+rec(left+1,right)+rec(right+1,r);
            val+=abs(al[left]-al[right]);
            res=min(res,val);
        }
    }
    return dp[l][r]=res;
}


